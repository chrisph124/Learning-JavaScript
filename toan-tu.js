//Danh sách các toán tử gán thường dùng trong JavaScript

//Toán tử ( = )
var a = 10;
var b = a;
console.log(b);

//Toán tử ( += )
var c = 10;
var d = 12;
// c += d --> c = c + d nên lúc này c = 10 + 12 = 22;
console.log(c += d);

//Toán tử ( -= )
//Tương tự như =+ và lúc này e = 15 - 10 = 5
var e = 15;
var f = 10;
console.log(e -= f);

